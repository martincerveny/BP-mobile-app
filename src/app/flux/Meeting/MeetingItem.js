/**
 * MeetingList
 */
class MeetingItem {
    getId() {
        return this.id;
    }

    getName() {
        return this.name;
    }

    getDate() {
        return this.date;
    }

    getTime() {
        return this.time;
    }

    getPlace() {
        return this.place;
    }

    getPeopleCount() {
        return this.peopleCount;
    }

    getNote() {
        return this.note;
    }
}

export default MeetingItem;