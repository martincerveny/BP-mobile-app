class UserItem {
    getId() {
        return this.id;
    }

    getMeetingIds() {
        return this.meetingIds;
    }

    getName() {
        return this.name;
    }

    getAge() {
        return this.age;
    }

    getAddress() {
        return this.address;
    }

    getCompany() {
        return this.company;
    }

    getNote() {
        return this.note;
    }
}

export default UserItem;