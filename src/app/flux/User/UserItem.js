/**
 * UserItem
 */
class UserItem {
    getId() {
        return this.id;
    }

    getMeetingIds() {
        return this.meetingIds;
    }

    getFirstName() {
        return this.firstName;
    }

    getLastName() {
        return this.lastName;
    }

    getImage() {
        return this.image;
    }

    getAge() {
        return this.age;
    }

    getAddress() {
        return this.address;
    }

    getCompany() {
        return this.company;
    }

    getNote() {
        return this.note;
    }
}

export default UserItem;